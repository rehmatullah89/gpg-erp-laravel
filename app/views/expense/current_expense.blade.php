      $("section[mysection=hide_n_show]").hide();
      $('#togglerButton').click(function(){
         $("section[mysection=hide_n_show]").toggle("slow");
         if ($('#toggle_div_plus').attr("class") == "fa fa-plus")
            $('#toggle_div_plus').removeClass('fa fa-plus').addClass('fa fa-minus');
         else 
            $('#toggle_div_plus').removeClass('fa fa-minus').addClass('fa fa-plus');
      }); 
      $('#Filter').on('change',function(){
        var vl = $(this).val();
        if (vl == 'emp') {
          $('#show_hide_val').html('<select name="emp" class="form-control">{{$emp_options}}</select>');
        }else if (vl == 'cus') {
          $('#show_hide_val').html('<select name="cus" class="form-control">{{$cus_options}}</select>');
        }else if (vl == 'job') {
          $('#show_hide_val').html('<input type="text" value="" name="job" id="job" class="form-control">');
        }else if (vl == 'expenseType') {
          $('#show_hide_val').html('<select name="expenseType" class="form-control">{{$expType_options}}</select>');
        }else{
          $('#show_hide_val').html('<input type="text" value="" name="FVal" id="FVal" class="form-control">');
        }
      });

      $('#reset_search_form').click(function(){
              $('#SDate').val("");
              $('#EDate').val("");
              $('#Filter').val("");
              $('#show_hide_val').html('<input type="text" value="" name="FVal" id="FVal" class="form-control">');
              $('#FVal').val("");
      });
    </script>
